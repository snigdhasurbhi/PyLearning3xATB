#While loop
print("While loop")
i = 1
while i <= 5:
    print(i)
    i += 1
# i=0, 0<= 5, 0
# i=1, 1<= 5, 1
# i=2, 2<= 5, 2
# i=3, 3<= 5, 3
# i=4, 4<= 5, 4
# i=5, 5<= 5, 5
# i=6, 6<= 5, loop terminate/ out of the loop
print("For loop")
for i in range(1, 6):
    print(i)
#i=1, till 6, =1
#i=2, till 6, =2



